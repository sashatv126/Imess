import Foundation
import UIKit
extension UIColor {
    static func button() -> UIColor {
        return #colorLiteral(red: 0.1764705926, green: 0.01176470611, blue: 0.5607843399, alpha: 1)
    }
    static func buttonJ() -> UIColor {
        return #colorLiteral(red: 0.1490196078, green: 0.1607843137, blue: 0.1529411765, alpha: 1)
    }
    static func buttonDark() -> UIColor {
        return #colorLiteral(red: 0.08903987753, green: 0.08756792423, blue: 0.1077388735, alpha: 1)
    }
    static func textField() -> UIColor {
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
}
